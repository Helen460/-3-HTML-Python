#Задачі на if-else, if-elif-else:
#1. Ввести три числа і знайти найменше серед них.
first = float(input("Введіть перше число: "))
second = float(input("Введіть друге число: "))
third = float(input("Введіть третє число: "))

if first <= second and first <= third:
    print("Найменше число: ", first)
elif second <= first and second <= third:
    print("Найменше число: ", second)
else:
    print("Найменше число: ", third)

#2. Ввести число і з’ясувати — воно позитивне, негативне чи нуль.
number = float(input("Введіть число: "))

if number > 0:
    print("Число позитивне")
elif number < 0:
    print("Число негативне")
else:
    print("Число нуль")

#3. Вводиться ціле число (не більше 4 розрядів!). Визначити кількість цифр у ньому.
number = int(input("Введіть ціле число (не більше 4 розрядів): "))

if 0 <= number < 10:
    print("Кількість цифр: 1")
elif 10 <= number < 100:
    print("Кількість цифр: 2")
elif 100 <= number < 1000:
    print("Кількість цифр: 3")
elif 1000 <= number < 10000:
    print("Кількість цифр: 4")
else:
    print("Число має бути не більше 4 розрядів")

