#Задачі на перетворення типів (без умов):
#1.Користувач вводить з клавіатури два дробових числа. Вивести на екран суму цілих частин та суму дробових. Наприклад, для чисел 12.34 та 56.78 це буде 68 та 1.12.
first = float(input("Введіть перше дробове число: "))
second = float(input("Введіть друге дробове число: "))

integer_1 = int(first)
fractional_1 = first - integer_1

integer_2 = int(second)
fractional_2 = second - integer_2

print("Сума цілих частин: ", integer_1 + integer_2)
print("Сума дробових частин: ", fractional_1 + fractional_2)

#2. Користувач вводить з клавіатури суму в гривнях (дробове число). Вивести цiлу кількість гривень та окремо кількість копійок. Наприклад, ввести 45.67 — вивести 45 гривень 67 копійок.
amount = float(input("Введіть суму в гривнях: "))

integer_grivny = int(amount)
fractional_kopiyky = round((amount - integer_grivny) * 100)

print("Ціла кількість гривень: ", integer_grivny, "гривень,", "кількість копійок: ", fractional_kopiyky, "копійок")

#3.Користувач вводить з клавіатури масу в тоннах (дробове число). Вивести кількість тонн, кілограмів, грамів. (Наприклад, ввести 126.456789т — вивести 126т 456кг 789г).
mass = float(input("Введіть масу в тоннах (дробове число): "))

tons = int(mass)
kilograms = int((mass - tons) * 1000)
grams = int((mass * 1000 - tons * 1000 - kilograms) * 1000)

print("Кількість тон, кілограмів та грамів: ", tons, "т", kilograms, "кг", grams, "г" )

